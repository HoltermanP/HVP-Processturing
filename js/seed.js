function _wp(project, apd, tracdeel, wp, start, eind, eng, lNieuw, mpw, ms) {
  const keys = ['overdrachtVO','analyseNaarVO','startConceptDO','conceptDO','onderzoekGereed','doNaarUO','eindeUO','contractGereed','werkvoorbGereed','uitvoeringGereed'];
  const mij = {};
  keys.forEach((k, i) => { mij[k] = ms[i] || ''; });
  return {
    id: `${project}|${wp}|${start}|${eind}`,
    project, apd, tracdeel, wp,
    tracStart: start, tracEind: eind,
    engineer: eng, lengteNieuw: lNieuw, mPerWeek: mpw,
    mijlpalen: mij,
  };
}

